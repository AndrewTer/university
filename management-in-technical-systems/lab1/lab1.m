%Построение амплитудно – фазочастотной характеристики

w = -50;
for i = 1:1000
    U(i) = (-200*w.^2)/(w.^4+300*w.^2+2500);
    V(i) = (500*w-10*w.^3)/(w.^4+300*w.^2+2500);
    w = w + 0.1;
end;

%Амплитудно-частотная характеристика системы
figure;
plot(U,V);
xlabel('U');
ylabel('V');
title('АЧХ');
grid on;

% Диаграмма  bode
 figure;
 bode(U,V);
 
% Вещественная характеристика U(w)
 figure;
 plot(U,'R');
 title('U(w)');
 grid on;
